function Notify(Title, Content, Duration, Icon)
	if Title == nil then
		Title = "Notification"
	end

	if Content == nil then
		Content = "This is a notification."
	end

	if Duration == nil or Duration <= 0 then
		Duration = NotificationDuration
	end

	if Icon == nil then
		Icon = NotificationIcon
	end

	UI_Framework:Notify({
		Title = Title,
		Content = Content,
		Duration = Duration,
		Icon = Icon,
	})
end

